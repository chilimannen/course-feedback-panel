package Model;

/**
 * @author Robin Duda
 *
 * Defines the duration of a vote.
 */
public class Duration {
    private long begin;
    private long end;

    public long getBegin() {
        return begin;
    }

    public void setBegin(long begin) {
        this.begin = begin;
    }

    public long getEnd() {
        return end;
    }

    public void setEnd(long end) {
        this.end = end;
    }
}
